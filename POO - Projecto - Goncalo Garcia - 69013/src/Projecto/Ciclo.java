package Projecto;

public class Ciclo {
	
	private int duracao;
	private double potencia;
	
	
	Ciclo(int duracao, double potencia) {
		super();
		this.duracao = duracao;
		this.potencia = potencia;
	}


	public int getDuracao() {
		return duracao;
	}

	public double getPotencia() {
		return potencia;
	}
	
	
	
	

}
